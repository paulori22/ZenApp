public class TextText2Model {

    private String text3, text4;

    public TextText2Model(String text3, String text4)
    {
        this.text3 = text3;
        this.text4 = text4;
    }

    public String getText3()
    {
        return text3;
    }

    public String getText4()
    {
        return text4;
    }
}
